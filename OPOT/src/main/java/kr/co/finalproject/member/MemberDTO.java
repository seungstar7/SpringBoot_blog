package kr.co.finalproject.member;

public class MemberDTO {

	private String mem_id;
	private String mem_pw;
	private String mem_phone;
	private String mem_email;
	private String mem_lv;
	private String mem_reg;
	private String mem_birth;
	private String new_pw;

	
	public MemberDTO() {}

	public String getMem_id() {
		return mem_id;
	}

	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	public String getMem_pw() {
		return mem_pw;
	}

	public void setMem_pw(String mem_pw) {
		this.mem_pw = mem_pw;
	}

	public String getMem_phone() {
		return mem_phone;
	}

	public void setMem_phone(String mem_phone) {
		this.mem_phone = mem_phone;
	}

	public String getMem_email() {
		return mem_email;
	}

	public void setMem_email(String mem_email) {
		this.mem_email = mem_email;
	}

	public String getMem_lv() {
		return mem_lv;
	}

	public void setMem_lv(String mem_lv) {
		this.mem_lv = mem_lv;
	}

	public String getMem_reg() {
		return mem_reg;
	}

	public void setMem_reg(String mem_reg) {
		this.mem_reg = mem_reg;
	}

	public String getMem_birth() {
		return mem_birth;
	}

	public void setMem_birth(String mem_birth) {
		this.mem_birth = mem_birth;
	}

	public String getNew_pw() {
		return new_pw;
	}

	public void setNew_pw(String new_pw) {
		this.new_pw = new_pw;
	}
	
	
	
	
}//class end
