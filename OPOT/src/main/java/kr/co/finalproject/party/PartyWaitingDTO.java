package kr.co.finalproject.party;

public class PartyWaitingDTO {
	String mem_id;
	String ott_name;
	int waiting_no;
	String waiting_date;
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getOtt_name() {
		return ott_name;
	}
	public void setOtt_name(String ott_name) {
		this.ott_name = ott_name;
	}
	public int getWaiting_no() {
		return waiting_no;
	}
	public void setWaiting_no(int waiting_no) {
		this.waiting_no = waiting_no;
	}
	public String getWaiting_date() {
		return waiting_date;
	}
	public void setWaiting_date(String waiting_date) {
		this.waiting_date = waiting_date;
	}
	
	

}
