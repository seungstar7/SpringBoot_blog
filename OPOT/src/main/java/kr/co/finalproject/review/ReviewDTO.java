package kr.co.finalproject.review;

public class ReviewDTO {
	
	private int rev_code;
	private String mem_id;
	private int mcode;
	private String rev_title;
	private String rev_reg;
	private String rev_cont;
	private String rev_spo;
	private int rev_rate;

	public ReviewDTO() {}

	public int getRev_code() {
		return rev_code;
	}




	public void setRev_code(int rev_code) {
		this.rev_code = rev_code;
	}




	public String getMem_id() {
		return mem_id;
	}




	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}




	public int getMcode() {
		return mcode;
	}




	public void setMcode(int mcode) {
		this.mcode = mcode;
	}




	public String getRev_title() {
		return rev_title;
	}




	public void setRev_title(String rev_title) {
		this.rev_title = rev_title;
	}




	public String getRev_reg() {
		return rev_reg;
	}




	public void setRev_reg(String rev_reg) {
		this.rev_reg = rev_reg;
	}




	public String getRev_cont() {
		return rev_cont;
	}




	public void setRev_cont(String rev_cont) {
		this.rev_cont = rev_cont;
	}




	public String getRev_spo() {
		return rev_spo;
	}




	public void setRev_spo(String rev_spo) {
		this.rev_spo = rev_spo;
	}




	public int getRev_rate() {
		return rev_rate;
	}




	public void setRev_rate(int rev_rate) {
		this.rev_rate = rev_rate;
	}


	
}//class end